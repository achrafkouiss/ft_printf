#include <stdio.h>

int	ft_count(int n)
{
	int	count;

    if (n < 10 && n >= 0)
    {
        return (0);
    }
    // else if (n > -10 && n < 0)
    // {
    //     return (1);
    // }
	count = 0;
	if (n < 0)
	{
		n = n * -1;
		count++; //=> 1
	}
	while (n > 0)// => 120 > 0 => 12 > 0 => 1 > 0
	{
        // printf("n =======> %d\n", n);
		n = n / 10; //=> 12 > 1 > 0
		count++; //=> 2 => 3 => 4
	}
    // printf("count =======> %d\n", count);
	return (count - 1); //=> 3
}